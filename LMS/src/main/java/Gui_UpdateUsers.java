    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean success = false;
        String oldemail = oldEmail_textbox.getText();
        String oldpassword = oldPassword_textbox.getText();
        String newemail = newEmail_textbox.getText();
        String newpassword = newPassword_textbox.getText();
        
        //if new password or email is the same, just assign old value
        if(oldEm){
            newemail = oldemail;
        }
        
        if(oldPass){
            newpassword = oldpassword;
        }
        
        
        try {
            success = la.UpdateUser(newemail,newpassword);
            if (success) {
                JOptionPane.showMessageDialog(this, "Successfully, the account is updated on our database");
                oldEmail_textbox.setText("");
                oldPassword_textbox.setText("");
                newEmail_textbox.setText("");
                newPassword_textbox.setText("");
                email = newemail;
                password = newpassword;
            } else if (newemail.isEmpty()||newpassword.isEmpty()||oldemail.isEmpty()||oldpassword.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please fill in the account's information for updating...");
            } else {
                JOptionPane.showMessageDialog(this, "Something went wrong... the account is not updated yet");
            }
        } catch (ClassNotFoundException ex) {
                            JOptionPane.showMessageDialog(this, "Class no found");
                            Logger.getLogger(Gui_UpdateUsers.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
                                        JOptionPane.showMessageDialog(this, "sql exception");

            Logger.getLogger(Gui_UpdateUsers.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        

    }//GEN-LAST:event_jButton1ActionPerformed

